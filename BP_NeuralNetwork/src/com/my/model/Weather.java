package com.my.model;

public class Weather {
	
	private String city;
	private String date;
	private String temphi;
	private String templow;

	private String description;
	private String windir;
	private String windstrength;
	private String aqi;
	private String aqiinfo;
	private String aqilevel;
	
	
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTemphi() {
		return temphi;
	}
	public void setTemphi(String temphi) {
		this.temphi = temphi;
	}
	public String getTemplow() {
		return templow;
	}
	public void setTemplow(String templow) {
		this.templow = templow;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getWindir() {
		return windir;
	}
	public void setWindir(String windir) {
		this.windir = windir;
	}
	public String getWindstrength() {
		return windstrength;
	}
	public void setWindstrength(String windstrength) {
		this.windstrength = windstrength;
	}
	public String getAqi() {
		return aqi;
	}
	public void setAqi(String aqi) {
		this.aqi = aqi;
	}
	public String getAqiinfo() {
		return aqiinfo;
	}
	public void setAqiinfo(String aqiinfo) {
		this.aqiinfo = aqiinfo;
	}
	public String getAqilevel() {
		return aqilevel;
	}
	public void setAqilevel(String aqilevel) {
		this.aqilevel = aqilevel;
	}
	
	

}
